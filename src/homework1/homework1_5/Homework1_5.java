package homework1.homework1_5;

public class Homework1_5 {
    public static void divisionWithRemainder(int q, int w) {
        int divisionResult = q / w;
        int remainderAfterDivision = q % w;
        System.out.println(q + " / " + w + " = " + divisionResult + " and the remainder is "
                + remainderAfterDivision + ".");
    }
}


